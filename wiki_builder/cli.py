def load_config(config_path: Optional[str] = None) -> dict:
    """Load .ku-wiki.toml from current dir or specified path."""
    paths = []
    if config_path:
        paths.append(Path(config_path))
    paths += [Path(".ku-wiki.toml"), Path.home() / ".ku-wiki.toml"]

    for p in paths:
        if p.exists():
            try:
                if sys.version_info >= (3, 11):
                    import tomllib
                    return tomllib.loads(p.read_text())
                else:
                    import tomli
                    return tomli.loads(p.read_text())
            except Exception:
                pass

    return {}


def get_client(config: dict, api_key: Optional[str] = None) -> KUClient:
    key = api_key or config.get("ku_api_key") or os.environ.get("KU_API_KEY", "")
    if not key:
        console.print("[red]❌ No KU API key found.[/red]")
        console.print("Set it via: [bold]export KU_API_KEY=ku_test_...[/bold]")
        console.print("Or add to .ku-wiki.toml: [bold]ku_api_key = \"ku_test_...\"[/bold]")
        sys.exit(1)
    return KUClient(api_key=key)


def get_vault(config: dict, vault_path: Optional[str] = None) -> str:
    return vault_path or config.get("vault_path") or os.environ.get("KU_WIKI_VAULT", DEFAULT_VAULT)


